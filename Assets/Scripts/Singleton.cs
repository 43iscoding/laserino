using UnityEngine;
using System.Collections;

public class Singleton : MonoBehaviour {

	public static Singleton instance;

    void Awake()
    {
        if (instance != null && instance != this)
        {
            Destroy(this.gameObject);
            return;
        }
        else
        {
            instance = this;
        }
        DontDestroyOnLoad(this.gameObject);
    }
}
